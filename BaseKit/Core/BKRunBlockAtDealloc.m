#import "BKRunBlockAtDealloc.h"

#import "BKMacrosDefinitions.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////
@implementation BKRunBlockAtDealloc

@synthesize block = _block;


////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)dealloc {
    if (nil != self.block) {
        self.block();
        self.block = nil;
    }
    
#if !BK_HAS_ARC
    [super dealloc];
#endif
}


////////////////////////////////////////////////////////////////////////////////////////////////////
- (id)initWithBlock:(BKBasicBlock)block {
    self = [super init];
    if (self) {
        self.block = block;
    }
    return self;
}


@end
